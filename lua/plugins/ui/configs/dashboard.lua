local season_now = "winter"
local month_now = os.date("*t")["month"]
-- if 3 <= month_now and month_now <= 5 then
-- 	season_now = "spring"
-- elseif 6 <= month_now and month_now <= 8 then
-- 	season_now = "summer"
-- elseif 9 <= month_now and month_now <= 11 then
-- 	season_now = "autumn"
-- else
-- 	season_now = "winter"
-- end
if 4 <= month_now and month_now <= 9 then
	season_now = "summer"
end

local logos_season = logos[season_now]
math.randomseed(os.time())
local random_index = math.random(#logos_season)
local logo = logos_season[random_index]

local lines = {}
for line in logo:gmatch("[^\n]+") do
	table.insert(lines, line)
end

local hour_now = os.date("*t")["hour"]
local greeting = ""
if 5 <= hour_now and hour_now < 11 then
	greeting = "Good morning"
elseif 12 <= hour_now and hour_now < 6 then
	greeting = "Good afternoon"
else
	greeting = "Good evening"
end

db.custom_header = lines
db.custom_center = {
	{ desc = greeting .. ", Neovim has been started.", action = "" },
	{ icon = "ïœ¡  ", desc = "Open new file", action = "DashboardNewFile" },
}
